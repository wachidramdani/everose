function cek_orderid() {
  var orderid = $('#orderid').val();
  var url = $('#form-tracking').attr('action');
  $('#form-tracking').attr('action',url+orderid);

  if(orderid == "") {
    $('#button-confirm').prop("disabled",true);
  } else {
    $('#button-confirm').prop("disabled",false);
  }
}

